    public function appointments()
    {
        $now = Carbon::now();
        $first = Event::where('datetime', 'LIKE', $now->toDateString().'%')->orderBy('datetime')->get();
        $second = Event::where('datetime', 'LIKE', $now->addDay(1)->toDateString().'%')->orderBy('datetime')->get();
        $third = Event::where('datetime', 'LIKE', $now->addDay(2)->toDateString().'%')->orderBy('datetime')->get();
        $fourth = Event::where('datetime', 'LIKE', $now->addDay(3)->toDateString().'%')->orderBy('datetime')->get();
        $fifth = Event::where('datetime', 'LIKE', $now->addDay(4)->toDateString().'%')->orderBy('datetime')->get();

        return view('admin.appointments.index', compact('first', 'second', 'third', 'fourth', 'fifth'));
    }

